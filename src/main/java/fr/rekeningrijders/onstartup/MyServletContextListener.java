    @Override
    public void contextInitialized(ServletContextEvent contextEvent) {
        int bsn = 1000;
        List<Owner> ownerList = new ArrayList<>(ownerArray.length);
        List<Vehicle> vehicleList = new ArrayList<>(vehicleArray.length);
        List<Tracker> trackerList = new ArrayList<>(vehicleArray.length);

        Random random = new Random(291671241438998L);
        byte[] buffer = new byte[10];
        for (String ownerInfo : ownerArray) {
            random.nextBytes(buffer);
            String uuid = UUID.nameUUIDFromBytes(buffer).toString();
            String[] fields = ownerInfo.split(",");
            Owner owner = new Owner(fields[0], fields[1], String.valueOf(bsn++), fields[3], fields[4], fields[5], uuid);
            ownerService.create(owner);
            ownerList.add(owner);
        }

        random = new Random(665198248186247L);
        for (int i = 0; i < vehicleArray.length; i += 1) {
            random.nextBytes(buffer);
            String uuid = "FR_" + UUID.nameUUIDFromBytes(buffer).toString();
            Tracker tracker = new Tracker(uuid);
            trackerService.create(tracker);
            trackerList.add(tracker);
        }
        for (int i = 0; i < Data.moreVehicles.length; i++) {
            random.nextBytes(buffer);
            String uuid = "FR_" + UUID.nameUUIDFromBytes(buffer).toString();
            Tracker tracker = new Tracker(uuid);
            trackerService.create(tracker);
            trackerList.add(tracker);
        }

        int ownerCounter = 0;
        int trackerCounter = 0;
        for (String vehicle_info : vehicleArray) {
            Owner owner = ownerList.get(ownerCounter++);
            String[] fields = vehicle_info.split(",");
            Tracker tracker = trackerList.get(trackerCounter++);
            Vehicle vehicle = new Vehicle(fields[0], fields[1], fields[2], Short.parseShort(fields[3]), tracker, owner);
            vehicleService.create(vehicle);
            vehicleList.add(vehicle);
            if (ownerCounter == ownerList.size()) ownerCounter = 0;
        }

        for (String user_info : userList) {
            String[] fields = user_info.split(",");
            User user = new User(fields[0], fields[1], fields[2]);
            userService.create(user);
        }

        List<InvoiceVehicle> ownedVehicles = new ArrayList<>();
        for (int i = 0; i < vehicleList.size(); i += ownerList.size()) {
            ownedVehicles.add(new InvoiceVehicle(vehicleList.get(i)));
        }
        invoiceStorage.create(new Invoice("bbb", ownerList.get(0).getId(), (short) 2019, Month.JANUARY, 3.5, ownedVehicles));

        for (String zone : zoneList) {
            ratesStorage.writeZone(new Zone(zone, 0.25));
        }
    }

